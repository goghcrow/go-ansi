package ansi

import "strings"

func Transform(style string, text string) string {
	set := mapper[style]
	if set == nil {
		return text
	}
	return strings.Map(func(r rune) rune {
		switch {
		case r >= 'a' && r <= 'z':
			return set[0+r-'a']
		case r >= 'A' && r <= 'Z':
			return set[26+r-'A']
		case r >= '0' && r <= '9':
			return set[52+r-'0']
		default:
			return r
		}
	}, text)
}

